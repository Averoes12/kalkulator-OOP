<?php 
	class kalkulator{
		
		public function plus($num1, $num2){
			return $num1+$num2;
		}

		public function minus($num1, $num2){
			return $num1-$num2;
		}

		public function times($num1, $num2){
			return $num1*$num2;
		}

		public function divide($num1, $num2){
			return $num1/$num2;
		}

		public function modulus($num1, $num2){
			return $num1%$num2;
		}
	}

 ?>